    public function login(Request $request){
        try{
            $user = Usuario::where('usuario','=',$request->get('usuario'))->where('contrasena','=',$request->get('contrasena'))->exists();
            return response()->json(['log'=>$user],200);

        }catch(Exception $e){
            return response()->json(['log'=>$e],500);
        }
    }

